public class Livro {
    //Declaração dos atributos da classe
    String titulo;
    String autor;

    //Métodos Getters e Setter dos atributos
    public String getTitulo() {
        return titulo;
    }
    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }
    public String getAutor() {
        return autor;
    }
    public void setAutor(String autor) {
        this.autor = autor;
    }
}